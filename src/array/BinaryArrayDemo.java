package array;

public class BinaryArrayDemo {
    public static void main(String[] args) {
        int[][] a = new int[3][2];
        int[] b = new int[5];
        for (int i = 0; i <b.length ; i++) {
            System.out.println(b[i]);
        }
        for (int i = 0; i <a.length ; i++) {
            System.out.println(a[i]);
        }


    }
}
